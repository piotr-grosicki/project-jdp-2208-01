package com.kodilla.ecommercee.controller;

import com.kodilla.ecommercee.domain.dto.CartNotFoundException;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.servlet.mvc.method.annotation.ResponseEntityExceptionHandler;

@ControllerAdvice
public class GlobalHttpErrorHandler extends ResponseEntityExceptionHandler {

    @ExceptionHandler(CartNotFoundException.class)
    public ResponseEntity<Object> handleTaskNotFoundException(CartNotFoundException cartNotFoundException) {
        return new ResponseEntity<>("Operation doesn't exist", HttpStatus.BAD_REQUEST);

    }



}
